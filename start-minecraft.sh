#!/bin/bash
# Minecraft Server Startup Script
# Supports Forge, Spigot, and Paper servers with Docker compatibility

# Default values for environment variables (can be overridden)
TYPE=${TYPE:-"FORGE"}
MCVERSION=${MCVERSION:-"1.20.1"}
FORGEVERSION=${FORGEVERSION:-"47.2.0"}
MEMORY=${MEMORY:-"2G"}
EULA=${EULA:-"false"}

echo "========================================"
echo "Minecraft Server Startup"
echo "========================================"
echo "Server Type:    $TYPE"
echo "Minecraft:      $MCVERSION"
[ "$TYPE" = "FORGE" ] && echo "Forge Version:  $FORGEVERSION"
echo "Memory:         $MEMORY"
echo "EULA Accepted:  $EULA"
echo "========================================"

# Function to create eula.txt if EULA=true
create_eula_if_needed() {
    if [ "$EULA" = "true" ] && [ ! -f eula.txt ]; then
        echo "Auto-accepting EULA (EULA=true)..."
        echo "# Generated by startup script" > eula.txt
        echo "eula=true" >> eula.txt
        echo "âœ“ eula.txt created"
    fi
}

# Function to handle errors
handle_error() {
    echo "âŒ ERROR: $1"
    exit 1
}

# Function to download with retry
download_with_retry() {
    local url="$1"
    local output="$2"
    local max_retries=3
    local retry_count=0
    
    while [ $retry_count -lt $max_retries ]; do
        echo "Download attempt $((retry_count + 1))/$max_retries..."
        if wget --progress=bar:force:noscroll "$url" -O "$output"; then
            return 0
        fi
        retry_count=$((retry_count + 1))
        if [ $retry_count -lt $max_retries ]; then
            echo "Retrying in 5 seconds..."
            sleep 5
        fi
    done
    return 1
}

# Create eula.txt if needed
create_eula_if_needed

# Wait for eula.txt if not present and EULA is not true
if [ ! -f eula.txt ]; then
    echo "âš ï¸  Waiting for EULA acceptance..."
    echo "   To auto-accept, set environment variable: EULA=true"
    while [ ! -f eula.txt ]; do
        echo "   Waiting for eula.txt to be created..."
        sleep 10
    done
    echo "âœ“ EULA accepted"
fi

# Start the server based on type
case "$TYPE" in
    "FORGE")
        if [ ! -f server.jar ]; then
            echo "ðŸ“¥ Installing Forge Server..."
            
            # ÐšÐ›Ð®Ð§Ð•Ð’ÐžÐ• Ð˜Ð—ÐœÐ•ÐÐ•ÐÐ˜Ð•: Ð¤Ð¾Ñ€Ð¼Ð¸Ñ€ÑƒÐµÐ¼ "Ð²ÐµÑ€ÑÐ¸ÑŽ Ð´Ð»Ñ ÑÑÑ‹Ð»ÐºÐ¸" Ð½Ð° Ð¾ÑÐ½Ð¾Ð²Ðµ ÑÑ‚Ð°Ð½Ð´Ð°Ñ€Ñ‚Ð½Ð¾Ð³Ð¾ Ñ„Ð¾Ñ€Ð¼Ð°Ñ‚Ð° Forge
            # Ð­Ñ‚Ð¾Ñ‚ Ñ„Ð¾Ñ€Ð¼Ð°Ñ‚ (1.20.1-47.2.0) ÑÐ²Ð»ÑÐµÑ‚ÑÑ Ð½Ð°Ð¸Ð±Ð¾Ð»ÐµÐµ Ñ€Ð°ÑÐ¿Ñ€Ð¾ÑÑ‚Ñ€Ð°Ð½ÐµÐ½Ð½Ñ‹Ð¼.
            FORGE_VERSION_STRING="${MCVERSION}-${FORGEVERSION}"
            FORGE_INSTALLER="forge-${FORGE_VERSION_STRING}-installer.jar"
            
            # ÐšÐ›Ð®Ð§Ð•Ð’ÐžÐ• Ð˜Ð—ÐœÐ•ÐÐ•ÐÐ˜Ð•: Ð˜ÑÐ¿Ð¾Ð»ÑŒÐ·ÑƒÐµÐ¼ Ð¸ÑÐ¿Ñ€Ð°Ð²Ð»ÐµÐ½Ð½Ñ‹Ð¹ URL
            # Ð¡Ñ‚Ð°Ñ€Ð°Ñ ÑÑ‚Ñ€ÑƒÐºÑ‚ÑƒÑ€Ð°: .../forge/${MCVERSION}-${FORGEVERSION}/...
            # Ð§Ð°ÑÑ‚Ð¾ ÐºÐ¾Ñ€Ñ€ÐµÐºÑ‚Ð½Ð°Ñ ÑÑ‚Ñ€ÑƒÐºÑ‚ÑƒÑ€Ð°: .../forge/${FORGE_VERSION_STRING}/...
            DOWNLOAD_URL="https://maven.minecraftforge.net/net/minecraftforge/forge/${FORGE_VERSION_STRING}/${FORGE_INSTALLER}"
            
            echo "   Generated Version String: ${FORGE_VERSION_STRING}"
            echo "   Attempting Download From: ${DOWNLOAD_URL}"
            
            # ... (Ð´Ð°Ð»ÐµÐµ Ð¸Ð´ÐµÑ‚ ÐºÐ¾Ð´ ÑÐºÐ°Ñ‡Ð¸Ð²Ð°Ð½Ð¸Ñ wget, Ñ‚Ð°ÐºÐ¾Ð¹ Ð¶Ðµ ÐºÐ°Ðº Ñƒ Ð²Ð°Ñ)
            if ! download_with_retry "${DOWNLOAD_URL}" "${FORGE_INSTALLER}"; then
                # Ð•ÑÐ»Ð¸ ÑÐºÐ°Ñ‡Ð¸Ð²Ð°Ð½Ð¸Ðµ Ð¿Ð¾ Ð¾ÑÐ½Ð¾Ð²Ð½Ð¾Ð¹ ÑÑ…ÐµÐ¼Ðµ Ð½Ðµ ÑƒÐ´Ð°Ð»Ð¾ÑÑŒ, Ð¿Ñ€Ð¾Ð±ÑƒÐµÐ¼ Ð°Ð»ÑŒÑ‚ÐµÑ€Ð½Ð°Ñ‚Ð¸Ð²Ð½Ñ‹Ð¹ URL
                echo "   Primary URL failed, trying alternative pattern..."
                ALTERNATIVE_URL="https://maven.minecraftforge.net/net/minecraftforge/forge/${MCVERSION}-${FORGEVERSION}-${MCVERSION}/${FORGE_INSTALLER}"
                echo "   Attempting: ${ALTERNATIVE_URL}"
                if ! download_with_retry "${ALTERNATIVE_URL}" "${FORGE_INSTALLER}"; then
                    handle_error "All download attempts for Forge installer failed. Please check MCVERSION (${MCVERSION}) and FORGEVERSION (${FORGEVERSION})."
                fi
            fi
            
            echo "ðŸ” Searching for Forge server jar..."
            
            # Patterns to search for Forge jar
            local patterns=(
                "forge-${MCVERSION}-${FORGEVERSION}.jar"
                "forge-${MCVERSION}-${FORGEVERSION}-universal.jar"
                "forge-${MCVERSION}-${FORGEVERSION}-server.jar"
                "forge.jar"
            )
            
